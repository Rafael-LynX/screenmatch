package listas_colecoes.Aula_3.Desafio_2;

public class Animal {
    String nome;
    String tipo;


    public Animal(String nome, String tipo) {
        this.nome = nome;
        this.tipo = tipo;
    }
    public String getNome() {
        return nome;
    }
    public String getTipo() {
        return tipo;
    }
    
}
